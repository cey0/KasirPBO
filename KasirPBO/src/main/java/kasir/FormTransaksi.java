    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try {

            // TODO:
            // ambil koneksi

            Connection conn =
                koneksi.getKoneksi();

            // TODO:
            // hitung total

            double total =
                Double.parseDouble(
                    txtTotal.getText()
                );

            // TODO:
            // ppn sementara 0

            double ppn = 0;

            // TODO:
            // insert transaksi

            String sqlTransaksi =
                "INSERT INTO transaksi(total, ppn) "
                + "VALUES (?, ?)";

            PreparedStatement psTransaksi =
                conn.prepareStatement(
                    sqlTransaksi,
                    Statement.RETURN_GENERATED_KEYS
                );

            psTransaksi.setDouble(1, total);
            psTransaksi.setDouble(2, ppn);

            psTransaksi.executeUpdate();

            // TODO:
            // ambil id transaksi

            ResultSet generatedKeys =
                psTransaksi.getGeneratedKeys();

            int transaksiId = 0;

            if (generatedKeys.next()) {
                transaksiId =
                    generatedKeys.getInt(1);
            }

            // TODO:
            // ambil model table

            DefaultTableModel model =
                (DefaultTableModel) tblKeranjang.getModel();

            // TODO:
            // looping semua item

            for (int i = 0; i < model.getRowCount(); i++) {

                int barangId =
                    Integer.parseInt(
                        model.getValueAt(i, 0).toString()
                    );

                int qty =
                    Integer.parseInt(
                        model.getValueAt(i, 2).toString()
                    );

                double subtotal =
                    Double.parseDouble(
                        model.getValueAt(i, 4).toString()
                    );

                // =========================================
                // INSERT DETAIL TRANSAKSI
                // =========================================

                String sqlDetail =
                    "INSERT INTO detail_transaksi "
                    + "(transaksi_id, barang_id, jumlah, subtotal) "
                    + "VALUES (?, ?, ?, ?)";

                PreparedStatement psDetail =
                    conn.prepareStatement(sqlDetail);

                psDetail.setInt(1, transaksiId);
                psDetail.setInt(2, barangId);
                psDetail.setInt(3, qty);
                psDetail.setDouble(4, subtotal);

                psDetail.executeUpdate();

                // =========================================
                // UPDATE STOK
                // =========================================

                String sqlUpdateStok =
                    "UPDATE barang "
                    + "SET stok = stok - ? "
                    + "WHERE id = ?";

                PreparedStatement psUpdate =
                    conn.prepareStatement(sqlUpdateStok);

                psUpdate.setInt(1, qty);
                psUpdate.setInt(2, barangId);

                psUpdate.executeUpdate();
            }

            // TODO:
            // notif sukses

            JOptionPane.showMessageDialog(
                this,
                "Checkout berhasil!"
            );

            // TODO:
            // kosongin table

            model.setRowCount(0);

            // TODO:
            // reset total

            txtJumlah.setText("0");
            txtTotal.setText("0");

            // TODO:
            // reload barang biar stok update

            loadBarang();

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                this,
                e.getMessage()
            );
        }
    }//GEN-LAST:event_jButton2ActionPerformed

